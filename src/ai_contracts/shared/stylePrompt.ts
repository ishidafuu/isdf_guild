export function buildSharedStylePrompt(): string {
  return `
共通の文体ルール:
- まず状況を明確にし、その後で空気や余韻を少し足す
- 何が起きているか、誰が何をしようとしているかを早めにわかるようにする
- ライトで読みやすい文体にする
- 人間味、軽い冗談、少し笑える抜けはあってよい
- ただし軽薄にしすぎず、世界観の手触りは保つ
- 大げさな演出、過剰な詩性、気取りすぎた比喩は避ける
- 箇条書きのように情報を切らず、段落同士がつながるように書く
- 一文ごとに視点や話題を飛ばしすぎず、前の文を受けて次の文が続くようにする
- 必要なら文はやや長くなってよい
- キャラクターごとの口調、観察の癖、温度差は出してよい
- 会話は独立した台詞の羅列ではなく、相手の反応や間を受けた応酬にする
- 説明不足で雰囲気だけに逃げない
`.trim();
}
